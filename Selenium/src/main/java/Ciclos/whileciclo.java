package Ciclos;

public class whileciclo {

	public static void main(String[] args) {
		int a = 1;
		int b = 2;
		
		// Ciclo while donde a sale cuando se cumple la condición de salida, la cúal es que sea menor o igual a 4
		while(a <= 4 || b == 6)
		{
			System.out.println("El valor de a es: " +a);
			System.out.println("El valor de b es: " +b);
			//Incrementa el valor de a en cada iteración
			a++;
			b++;
			
		}

	}

}
