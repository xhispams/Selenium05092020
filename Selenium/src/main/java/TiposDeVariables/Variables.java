package TiposDeVariables;

public class Variables {

	public static void main(String[] args) {
    	//variable byte
    	byte x;
    	x = 12;
    	System.out.println(x);
    	
    	//variable short
    	short y;
    	//short y = 130;
    	y = 130;
    	System.out.println(y);
    	
    	//variable integer
    	int z;
    	z = 98345;
    	System.out.println(z);
    	
    	//variable long
    	long a = 98345333;
    	System.out.println(a);

    	//variable float
    	float base = 98345333;
    	System.out.println(base);

    	//variable double
    	double altura = 98345333d;
    	System.out.println(altura);

    	//variable char
    	char letraA = 'A';
    	System.out.println(letraA);
    	
    	//variable string
    	String cadenaTexto = "Aasdasdasd";
    	System.out.println(cadenaTexto);
    	
    	//variable booleano
    	boolean AvesVuelan = false;
    	System.out.println("Las aves vuelan? " + AvesVuelan);

	}

}
