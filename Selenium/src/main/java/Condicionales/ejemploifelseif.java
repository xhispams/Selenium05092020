package Condicionales;

public class ejemploifelseif {

	public static void main(String[] args) {
		int i = 28;
		
		if(i == 10) {
			System.out.println("El valor es 10");
		}else if(i == 15) {
			System.out.println("El valor es 15");
		}else if (i == 20) {
			System.out.println("El valor es 20");
		}else {
			System.out.println("No es ninguno de los valores");
		}

	}

}
